RSpec.describe 'test' do
  it 'test with "5 4\n6\n1 3 2 4\n1 5 1 1\n5 5 1 4\n4 4 2 2\n5 5 4 4\n1 5 1 4\n"' do
    io = IO.popen("ruby abc269/F.rb", "w+")
    io.puts("5 4\n6\n1 3 2 4\n1 5 1 1\n5 5 1 4\n4 4 2 2\n5 5 4 4\n1 5 1 4\n")
    io.close_write
    expect(io.readlines.join).to eq("28\n27\n36\n14\n0\n104\n")
  end

  it 'test with "1000000000 1000000000\n3\n1000000000 1000000000 1000000000 1000000000\n165997482 306594988 719483261 992306147\n1 1000000000 1 1000000000\n"' do
    io = IO.popen("ruby abc269/F.rb", "w+")
    io.puts("1000000000 1000000000\n3\n1000000000 1000000000 1000000000 1000000000\n165997482 306594988 719483261 992306147\n1 1000000000 1 1000000000\n")
    io.close_write
    expect(io.readlines.join).to eq("716070898\n240994972\n536839100\n")
  end

  it 'test with "999999999 999999999\n3\n999999999 999999999 999999999 999999999\n216499784 840031647 84657913 415448790\n1 999999999 1 999999999\n"' do
