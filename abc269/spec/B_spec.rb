RSpec.describe 'test' do
  it 'test with "..........\n..........\n..........\n..........\n...######.\n...######.\n...######.\n...######.\n..........\n..........\n"' do
    io = IO.popen("ruby abc269/B.rb", "w+")
    io.puts("..........\n..........\n..........\n..........\n...######.\n...######.\n...######.\n...######.\n..........\n..........\n")
    io.close_write
    expect(io.readlines.join).to eq("5 8\n4 9\n")
  end

  it 'test with "..........\n..#.......\n..........\n..........\n..........\n..........\n..........\n..........\n..........\n..........\n"' do
    io = IO.popen("ruby abc269/B.rb", "w+")
    io.puts("..........\n..#.......\n..........\n..........\n..........\n..........\n..........\n..........\n..........\n..........\n")
    io.close_write
    expect(io.readlines.join).to eq("2 2\n3 3\n")
  end

  it 'test with "##########\n##########\n##########\n##########\n##########\n##########\n##########\n##########\n##########\n##########\n"' do
    io = IO.popen("ruby abc269/B.rb", "w+")
    io.puts("##########\n##########\n##########\n##########\n##########\n##########\n##########\n##########\n##########\n##########\n")
    io.close_write
    expect(io.readlines.join).to eq("1 10\n1 10\n")
  end

end
