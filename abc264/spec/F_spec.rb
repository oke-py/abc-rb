  it 'test with "3 4\n4 3 5\n2 6 7 4\n0100\n1011\n1010\n"' do
    io = IO.popen("ruby abc264/F.rb", "w+")
    io.puts("3 4\n4 3 5\n2 6 7 4\n0100\n1011\n1010\n")
    io.close_write
    expect(io.readlines.join).to eq("9\n")
